def reversed(num):
    return int(str(num)[::-1])
def is_adam(num):
    square=num**2
    reverse_num=reversed(num)
    reversed_square=reverse_num**2

    return reversed(square)==reversed_square

n=int(input("Enter number:"))
if is_adam(n):
    print(f"{n} is an Adam number")
else:
    print(f"{n} isnot an Adam number")